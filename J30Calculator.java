public class J30Calculator {
    public static void main(String[] args) {
        int a = 56, b = 87;
        char oper = '+';
        switch (oper) {
            case '+':
                System.out.println(a + b);

                break;
            case '-':
                System.out.println(a - b);

                break;
            case '*':
                System.out.println(a + b);

                break;
            case '/':
                System.out.println(a + b);

                break;
            case '%':
                System.out.println(a + b);

                break;
            default:
                System.out.println("Invalid Input");

        }

    }
}
